const SIDEBAR_COOKIE_NAME = "sidebar_state"
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
const SIDEBAR_WIDTH = "280px"
const SIDEBAR_WIDTH_MOBILE = "280px"
const SIDEBAR_WIDTH_ICON = "80px"
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

type SidebarContext = {
  isExpanded: boolean
  setIsExpanded: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
}

const SidebarContext = React.createContext<SidebarContext | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }
  return context
}

const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    defaultExpanded?: boolean
    isExpanded?: boolean
    onExpandedChange?: (open: boolean) => void
  }
>(
  (
    {
      defaultExpanded = true,
      isExpanded: isExpandedProp,
      onExpandedChange: setIsExpandedProp,
      className,
      style,
      children,
      ...props
    },
    ref
  ) => {
    const isMobile = useIsMobile()
    const [openMobile, setOpenMobile] = React.useState(false)

    const [_isExpanded, _setIsExpanded] = React.useState(defaultExpanded)
    const isExpanded = isExpandedProp ?? _isExpanded
    const setIsExpanded = React.useCallback(
      (value: boolean | ((value: boolean) => boolean)) => {
        const expandedState = typeof value === "function" ? value(isExpanded) : value
        if (setIsExpandedProp) {
          setIsExpandedProp(expandedState)
        } else {
          _setIsExpanded(expandedState)
        }
        document.cookie = `${SIDEBAR_COOKIE_NAME}=${expandedState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`
      },
      [setIsExpandedProp, isExpanded]
    )

    const toggleSidebar = React.useCallback(() => {
      return isMobile
        ? setOpenMobile((open) => !open)
        : setIsExpanded((open) => !open)
    }, [isMobile, setIsExpanded, setOpenMobile])

    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault()
          toggleSidebar()
        }
      }
      window.addEventListener("keydown", handleKeyDown)
      return () => window.removeEventListener("keydown", handleKeyDown)
    }, [toggleSidebar])

    const contextValue = React.useMemo<SidebarContext>(
      () => ({
        isExpanded,
        setIsExpanded,
        isMobile,
        openMobile,
        setOpenMobile,
        toggleSidebar,
      }),
      [isExpanded, setIsExpanded, isMobile, openMobile, setOpenMobile, toggleSidebar]
    )

    return (
      <SidebarContext.Provider value={contextValue}>
        <TooltipProvider delayDuration={0}>
          <div
            style={
              {
                "--sidebar-width": SIDEBAR_WIDTH,
                "--sidebar-width-icon": SIDEBAR_WIDTH_ICON,
                ...style,
              } as React.CSSProperties
            }
            className={cn(
              "group/sidebar-wrapper flex min-h-svh w-full",
              className
            )}
            ref={ref}
            {...props}
          >
            {children}
          </div>
        </TooltipProvider>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"

const Sidebar = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    side?: "left" | "right"
  }
>(
  (
    {
      side = "left",
      className,
      children,
      ...props
    },
    ref
  ) => {
    const { isMobile, isExpanded, openMobile, setOpenMobile } = useSidebar()

    if (isMobile) {
      return (
        <Sheet open={openMobile} onOpenChange={setOpenMobile} {...props}>
          <SheetContent
            data-sidebar="sidebar"
            data-mobile="true"
            className="w-[--sidebar-width] bg-sidebar p-0 text-sidebar-foreground [&>button]:hidden"
            style={
              {
                "--sidebar-width": SIDEBAR_WIDTH_MOBILE,
              } as React.CSSProperties
            }
            side={side}
          >
            <SheetTitle className="sr-only">Menu</SheetTitle>
            <div className="flex h-full w-full flex-col">{children}</div>
          </SheetContent>
        </Sheet>
      )
    }

    return (
      <div
        ref={ref}
        className={cn("peer hidden shrink-0 md:block text-sidebar-foreground transition-[width] duration-300 ease-in-out", 
            isExpanded ? 'w-[var(--sidebar-width)]' : 'w-[var(--sidebar-width-icon)]',
            className
        )}
        data-expanded={isExpanded}
        data-side={side}
      >
        <div data-sidebar="sidebar" className="flex h-full flex-col border-r bg-sidebar">
          {children}
        </div>
      </div>
    )
  }
)
Sidebar.displayName = "Sidebar"

const SidebarTrigger = React.forwardRef<
  React.ElementRef<typeof Button>,
  React.ComponentProps<typeof Button>
>(({ className, onClick, ...props }, ref) => {
  const { toggleSidebar } = useSidebar()

  return (
    <Button
      ref={ref}
      data-sidebar="trigger"
      variant="ghost"
      size="icon"
      className={cn("h-8 w-8", className)}
      onClick={(event) => {
        onClick?.(event)
        toggleSidebar()
      }}
      {...props}
    >
      <PanelLeft />
      <span className="sr-only">Buka/Tutup Sidebar</span>
    </Button>
  )
})
SidebarTrigger.displayName = "SidebarTrigger"

